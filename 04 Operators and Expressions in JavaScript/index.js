// Arithmetic Operators
console.log("Operators in Js");
let a = 10;
let b = 4;
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a / b =", a / b);
console.log("a * b =", a * b);
console.log("a ** b =", a ** b);
console.log("a % b =", a % b);
console.log("++a =", ++a);
console.log("a++ =", a++);
console.log("--a =", --a);
console.log("a-- =", a--);

